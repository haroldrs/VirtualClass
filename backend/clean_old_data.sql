WHERE FECHA_RESPUESTA < '2026-06-01' 
   OR ID_TEMA IN (SELECT ID_TEMA FROM TEMA_FORO WHERE FECHA_CREACION < '2026-06-01' 
                  OR ID_FORO IN (SELECT ID_FORO FROM FORO WHERE FECHA_CREACION < '2026-06-01'));

