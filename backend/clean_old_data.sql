-- Script SQL para eliminar datos registrados o fechados antes de Junio (2026-06-01)
-- IMPORTANTE: Ejecutar dentro de una transacción para mantener la integridad referencial.

BEGIN;

-- 1. Asistencias
DELETE FROM ASISTENCIA 
WHERE FECHA < '2026-06-01' 
   OR ID_SESION IN (SELECT ID_SESION FROM SESION_CLASE WHERE FECHA < '2026-06-01');

-- 2. Sesiones de clase
DELETE FROM SESION_CLASE WHERE FECHA < '2026-06-01';

-- 3. Calificaciones y Notas
