-- 4. Entregas de Evaluaciones
DELETE FROM ENTREGA_EVALUACION 
WHERE FECHA_ENTREGA < '2026-06-01' 
   OR ID_EVALUACION IN (SELECT ID_EVALUACION FROM EVALUACION WHERE FECHA_EVALUACION < '2026-06-01');

-- 5. Evaluaciones
DELETE FROM EVALUACION WHERE FECHA_EVALUACION < '2026-06-01';

-- 6. Recursos Publicados
DELETE FROM RECURSOS WHERE FECHA_PUBLICACION < '2026-06-01';

-- 7. Eventos de Calendario Académico
